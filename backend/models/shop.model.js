
const mongoose=require("mongoose")
const shopSchema=new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    image:{
        type:String,
        required:true
    },
    owner:{
        type:mongoose.Schema.Types.objectId,
        ref:"User",
        required:true
    },
    city:{
        type:String,
        required:true
    },
    state:{
        type:String,
        required:true
    },
    address:{
        type:String,
        required:true
    },
    Items:[{
        type:mongoose.Schema.Types.objectId,
        ref:"Item"
    }]

},{timestamps:true})

const Shop=mongoose.model("Shop",shopSchema)

module.exports=Shop
